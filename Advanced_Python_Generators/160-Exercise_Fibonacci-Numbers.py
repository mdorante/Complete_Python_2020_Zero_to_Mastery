# Create a generator function that returns all the numbers in the Fibonacci sequence
# The function takes a a number as a parameter and does the fib sequence that number of times


def fib(number):
    a = 0
    b = 1
    for i in range(number):
        yield a
        temp = a
        a = b
        b = temp + b


for x in fib(21):
    print(x)
