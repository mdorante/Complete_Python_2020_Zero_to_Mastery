# OOP

# Everything in Python is an object

# Class: custom data types
# Naming convention, use Capitalized names for Classes and use CamelCase instead of snake_case

# The class is the blueprint of what we want to create
# basic properties, methods, etc. for the objects


class BigObject:  # Defining a class
    pass

# After we define the class, we have to instantiate objects that use that blueprint


obj1 = BigObject()  # instantiating an object
obj2 = BigObject()
obj3 = BigObject()
print(type(obj1))
# Output:
# <class '__main__.BigObject'>
