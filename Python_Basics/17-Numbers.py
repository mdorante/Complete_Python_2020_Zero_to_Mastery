#Fundamental Data Types
# int and float
print(type(2 + 4)) #int
print(type(2 - 4)) #int
print(type(2 * 4)) #int
print(type(2 / 4)) #float

print(type(1.09128)) #float

#floats take up much more memory space than ints 

print() #skips a line (actually it just writes an empty line)

# float + float = float
print(type(4.1 + 1.9)) #float

# float + int   = int
print(type(1.048 - 2)) #float

# int   + int   = int
print(type(7 + 8)) #int

print()

# exponent
print(2 ** 3) #8

# flat division (rounds down all decimals)
print(5 // 4) #1

# modulo (remainder)
print(6 % 4) #2